    public String getProdbajo() {
        return prodbajo;
    }

    public void setProdbajo(String prodbajo) {
        this.prodbajo = prodbajo;
    }

    public String getClireg() {
        return clireg;
    }

    public void setClireg(String clireg) {
        this.clireg = clireg;
    }

