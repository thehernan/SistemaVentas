     public void valida(){
         try {
             double cant = Double.parseDouble(jtfapertura.getText());
             if(cant >=0)
             {
                 jbtnaceptar.setEnabled(true);
             
             }else {
                 jbtnaceptar.setEnabled(false);
             
             }
             
             
         } catch (Exception e) {
             jbtnaceptar.setEnabled(false);
         }
     
     }
